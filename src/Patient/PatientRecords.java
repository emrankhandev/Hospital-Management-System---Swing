    public void loadData() {
        String sql = "select patient_id, patient_name, age, gender, address, phone_no, date from patient";
        try {
            PreparedStatement ps = DBCon.getConnection().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            Object columnData[] = new Object[7];
            while (rs.next()) {
                columnData[0] = rs.getInt("patient_id");
                columnData[1] = rs.getString("patient_name");
                columnData[2] = rs.getString("age");
                columnData[3] = rs.getString("gender");
                columnData[4] = rs.getString("address");
                columnData[5] = rs.getString("phone_no");
                columnData[6] = rs.getString("date");
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String searchField = search.getText();
        String sql = "Delete from patient where patient_id ='" + searchField + "'";
        try {
            PreparedStatement stmt = DBCon.getConnection().prepareStatement(sql);
            stmt.execute();
            JOptionPane.showMessageDialog(null, "Patient " + searchField + " has been deleted");
            defaultTableModel.getDataVector().removeAllElements();
            defaultTableModel.fireTableDataChanged();
            loadData();
            DBCon.getConnection().close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "patient id " + searchField + " not found");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String sql = "update patient set patient_name = ?, age = ?, gender = ?, address = ?, phone_no = ?, date = ? where patient_id = ?"; 
        
        try {
            PreparedStatement ps = DBCon.getConnection().prepareStatement(sql);
            ps.setString(1, pName.getText());
            ps.setString(2, age.getText());
            ps.setString(3, gender.getSelectedItem().toString());
            ps.setString(4, address.getText());
            ps.setString(5, pNo.getText());
            ps.setDate(6, new Date(date.getDate().getTime()));
            ps.setString(7, pId.getText());
            ps.executeUpdate();
            table_update();
            JOptionPane.showMessageDialog(null,"Patiet Updated");
            table_update();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

     private void table_update() {
        int CC;
        String sql = "SELECT * FROM patient";
        try {
            PreparedStatement pst =DBCon.getConnection().prepareStatement(sql);
            ResultSet Rs = pst.executeQuery();  
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("patient_id"));
                    v2.add(Rs.getString("patient_name"));
                    v2.add(Rs.getString("age"));
                    v2.add(Rs.getString("gender"));
                    v2.add(Rs.getString("address"));
                    v2.add(Rs.getString("phone_no"));
                    v2.add(Rs.getString("date"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
